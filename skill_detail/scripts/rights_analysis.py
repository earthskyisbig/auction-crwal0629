# -*- coding: utf-8 -*-
"""등기부 권리분석 엔진 (B안 핵심).

입력: 등기부에서 파싱한 '살아있는' 권리 목록 + 경매개시일 + (선택)명세서 임차인.
출력: 말소기준권리, 각 권리의 소멸/인수 판정, 정밀 인수금.

⚠️ 표준 경매 권리분석 규칙을 코드화한 것이며, 특수물건(유치권·법정지상권·분묘기지권 등
   등기부에 안 나오는 권리)은 별도 현장확인이 필요하다. 배당요구·배당순위에 따른 전세권/
   임차권 소멸 여부는 명세서·배당표로 최종 확인해야 한다.

권리 entry 스키마:
  {'구분':'갑구'|'을구', '순위번호':'1', '등기목적':'근저당권설정',
   '접수일':(YYYY,M,D), '금액': int|None, '권리자': str, '말소여부': bool}
"""

# 말소기준권리가 될 수 있는 권리(=매각으로 항상 소멸, 최선순위면 기준선)
_MALSO_BASE = ('저당권', '근저당권', '압류', '가압류', '담보가등기', '경매개시결정', '경매기입')
# 말소기준보다 앞서면 '인수'되는 권리(용익물권·보전처분·보전가등기 등)
_INSU_IF_SENIOR = ('전세권', '지상권', '지역권', '임차권', '가처분', '환매', '소유권이전청구권')
# 이들은 등기목적에 '가등기'가 있어도 담보가등기가 아니라 '보전(소유권이전청구권)가등기'로 인수 대상


def _d(x):
    """접수일 튜플 정규화."""
    return tuple(x) if x else None


def _is(등기목적, keys):
    return any(k in (등기목적 or '') for k in keys)


def analyze_rights(entries, 경매개시일=None, 명세서임차인=None):
    """entries(살아있는 권리) → 말소기준·소멸/인수·인수금.

    명세서임차인: [{'성명','보증금','전입신고일'(YYYY,M,D 튜플|None),'배당요구일'...}]
    """
    live = [e for e in entries if not e.get('말소여부')]

    # 1) 말소기준권리 = 소멸성 권리 중 접수일 최선순위. 없으면 경매개시결정.
    base_cands = [e for e in live if _is(e['등기목적'], _MALSO_BASE)]
    base = min(base_cands, key=lambda e: _d(e['접수일']) or (9999, 99, 99)) if base_cands else None
    base_date = _d(base['접수일']) if base else _d(경매개시일)

    # 2) 각 권리 소멸/인수 판정
    results, 인수금 = [], 0
    for e in live:
        edate = _d(e['접수일'])
        목적 = e['등기목적']
        senior = (edate is not None and base_date is not None and edate <= base_date)
        if _is(목적, _MALSO_BASE):
            판정, 사유 = '소멸', '(근)저당·(가)압류·담보가등기·경매개시 → 매각으로 소멸'
        elif _is(목적, _INSU_IF_SENIOR):
            if senior:
                판정 = '인수'
                사유 = '말소기준보다 선순위 용익물권/보전처분 → 매수인 인수'
                if _is(목적, ('전세권',)) and e.get('금액'):
                    인수금 += e['금액']; 사유 += f" (전세금 {e['금액']:,}원 인수)"
                elif _is(목적, ('임차권',)) and e.get('금액'):
                    인수금 += e['금액']; 사유 += f" (임차보증금 {e['금액']:,}원 인수)"
                else:
                    사유 += ' (금액 아닌 권리 자체 인수 — 가처분/보전가등기/지상권 등 주의)'
            else:
                판정, 사유 = '소멸', '말소기준보다 후순위 → 매각으로 소멸'
        else:  # 소유권이전 등 분석대상 아님
            판정, 사유 = '해당없음', '소유권 등 분석대상 아님'
        results.append({**e, '판정': 판정, '사유': 사유, '선순위': senior})

    # 3) 대항력 임차인(명세서) 인수 — 전입 < 말소기준
    임차인인수 = []
    for t in (명세서임차인 or []):
        ji = _d(t.get('전입신고일'))
        if ji and base_date and ji < base_date:
            보증금 = t.get('보증금') or 0
            # 배당요구+확정일자로 회수 가능성 있으면 '조건부'(보수적으로 상한 계산)
            cond = bool(t.get('배당요구일') and t.get('확정일자'))
            임차인인수.append({'성명': t.get('성명'), '보증금': 보증금,
                            '회수가능성': cond})
            인수금 += 보증금

    return {
        '말소기준권리': (f"{base['등기목적']} ({base['접수일'][0]}.{base['접수일'][1]}.{base['접수일'][2]})"
                    if base else ('경매개시결정' if 경매개시일 else '판정불가(소멸성 권리 없음)')),
        '말소기준일': base_date,
        '권리목록': results,
        '인수권리': [r for r in results if r['판정'] == '인수'],
        '소멸권리': [r for r in results if r['판정'] == '소멸'],
        '대항력임차인': 임차인인수,
        '정밀인수금': 인수금,
        '_주의': '등기부 미기재 권리(유치권·법정지상권 등)·배당순위는 별도 확인. 전세권/임차권은 배당요구 시 소멸 가능(보수적 상한 계산).',
    }


# ── 합성 데이터 자체검증 ─────────────────────────────────────────────
def _selftest():
    cases = []

    # 케이스1: 선순위 근저당(말소기준) → 이후 전부 소멸, 인수 0
    e1 = [
        {'구분': '을구', '순위번호': '1', '등기목적': '근저당권설정', '접수일': (2020, 11, 4), '금액': 100000000, '권리자': 'A은행', '말소여부': False},
        {'구분': '갑구', '순위번호': '3', '등기목적': '가압류', '접수일': (2023, 5, 1), '금액': 50000000, '권리자': 'B', '말소여부': False},
        {'구분': '갑구', '순위번호': '4', '등기목적': '강제경매개시결정', '접수일': (2025, 6, 25), '금액': None, '권리자': 'B', '말소여부': False},
    ]
    r = analyze_rights(e1, 경매개시일=(2025, 6, 25))
    cases.append(('선순위 근저당 → 인수 0', r['말소기준권리'].startswith('근저당권'), r['정밀인수금'] == 0))

    # 케이스2: 선순위 전세권(말소기준=후순위 근저당) → 전세권 인수
    e2 = [
        {'구분': '을구', '순위번호': '1', '등기목적': '전세권설정', '접수일': (2019, 3, 1), '금액': 200000000, '권리자': 'C', '말소여부': False},
        {'구분': '을구', '순위번호': '2', '등기목적': '근저당권설정', '접수일': (2021, 7, 10), '금액': 80000000, '권리자': 'D은행', '말소여부': False},
    ]
    r = analyze_rights(e2, 경매개시일=(2025, 1, 1))
    cases.append(('선순위 전세권 인수 2억', r['말소기준권리'].startswith('근저당권'), r['정밀인수금'] == 200000000))

    # 케이스3: 이미 말소된 권리는 무시
    e3 = [
        {'구분': '을구', '순위번호': '1', '등기목적': '근저당권설정', '접수일': (2018, 1, 1), '금액': 300000000, '권리자': 'E', '말소여부': True},
        {'구분': '을구', '순위번호': '2', '등기목적': '근저당권설정', '접수일': (2022, 2, 2), '금액': 90000000, '권리자': 'F', '말소여부': False},
    ]
    r = analyze_rights(e3, 경매개시일=(2025, 1, 1))
    cases.append(('말소된 권리 제외 → 기준 2022', r['말소기준일'] == (2022, 2, 2), r['정밀인수금'] == 0))

    # 케이스4: 대항력 임차인(전입<말소기준) 인수
    e4 = [{'구분': '을구', '순위번호': '1', '등기목적': '근저당권설정', '접수일': (2023, 11, 7), '금액': 50000000, '권리자': 'G', '말소여부': False}]
    tenant = [{'성명': '홍길동', '보증금': 315000000, '전입신고일': (2022, 3, 25), '확정일자': (2022, 2, 28), '배당요구일': None}]
    r = analyze_rights(e4, 경매개시일=(2025, 6, 25), 명세서임차인=tenant)
    cases.append(('대항력 임차인 인수 3.15억', len(r['대항력임차인']) == 1, r['정밀인수금'] == 315000000))

    # 케이스5: 선순위 가처분 인수(금액 아님)
    e5 = [
        {'구분': '갑구', '순위번호': '2', '등기목적': '가처분', '접수일': (2020, 1, 1), '금액': None, '권리자': 'H', '말소여부': False},
        {'구분': '을구', '순위번호': '1', '등기목적': '근저당권설정', '접수일': (2021, 1, 1), '금액': 100000000, '권리자': 'I', '말소여부': False},
    ]
    r = analyze_rights(e5, 경매개시일=(2025, 1, 1))
    insu = [x for x in r['인수권리'] if '가처분' in x['등기목적']]
    cases.append(('선순위 가처분 인수(금액X)', len(insu) == 1, r['정밀인수금'] == 0))

    print("=== 권리분석 엔진 자체검증 ===")
    allok = True
    for name, *checks in cases:
        ok = all(checks)
        allok &= ok
        print(f"  [{'✅' if ok else '❌'}] {name}  {checks}")
    print("전체:", "통과 ✅" if allok else "실패 ❌")
    return allok


if __name__ == '__main__':
    _selftest()
